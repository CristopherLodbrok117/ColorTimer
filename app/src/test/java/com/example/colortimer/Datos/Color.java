package com.example.colortimer.Datos;

public class Color {
    private int id;
    private String color;

    public Color(){
    }

    public void setId(int id) {
        this.id = id;
    }
    public void setColor(String color) {
        this.color = color;
    }

    public int getId() {
        return id;
    }
    public String getColor() {
        return color;
    }
}
