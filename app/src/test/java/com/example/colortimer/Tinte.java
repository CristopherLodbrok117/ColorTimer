package com.example.colortimer;

public class Tinte {
    private int id;
    private String marca;

    public Tinte(){
    }

    public void setId(int id){
        this.id = id;
    }
    public void setMarca(String marca){
        this.marca = marca;
    }

    public int getId(){
        return id;
    }
    public String getMarca(){
        return  marca;
    }
}
